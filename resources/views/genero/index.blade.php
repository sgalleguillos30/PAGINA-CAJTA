      <p class="text-justify opensans">Por eso hemos levantado este espacio, con el objetivo de promover la
        igualdad de género y no discriminación el mes de marzo de 2025, para destacar la relevancia que ha sido
        el rol
        de la
        mujer profesional de CAJTA y en relación a las usuarias en la búsqueda de mejores condiciones de vida
        para
        ellas y sus familias, en la lucha la consolidación de la clase media, mediante su incorporación al
        trabajo
        remunerado y su liderazgo en todos los espacios de la sociedad civil. Sin embargo, son muchos los
        desafíos aún
        pendientes.
        La pobreza todavía afecta en mayor medida a las mujeres, las brechas remuneraciones siguen siendo muy
        elevadas, así como también los diferentes tipos de violencia hacia las mujeres y disidencias sexo
        genéricas.
      </p>

