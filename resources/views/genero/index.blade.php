          relativas a ello, esto en concordancia a las Políticas de Gobierno impulsadas en las últimas décadas
          que ha
          generado una transformación significativa para las Mujeres chilenas y residentes en nuestro país:
        </p>

        <!--lista avances-->
        <li class="text-justify">
          <span class="text-bold py-2">Avances en la igualdad ante la ley</span>
        </li>
        <li class="text-justify py-2">
          <span class="text-bold">Valoración e impulso a su incorporación plena a la vida pública en todos los
            ámbitos.</span>
        </li>
        <li class="text-justify py-2">
          <span class="text-bold">Mayor autonomía personal e independencia económica con la incorporación cada
