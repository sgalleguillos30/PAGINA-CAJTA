      <h3 class="title text-center">Diversidad</h3>
      <p class="text-justify opensans">En CAJTA, el día 28 de junio, celebramos el día mundial de la Diversidad
        Sexual
        (
        ORGULLO), como institución
        nos encontramos comprometidos a visibilizar, celebrar  y rememorar  la lucha por los derechos de la
        comunidad LGBTQINBQ+ y disidencias sexo genéricas. Es por ello que recordamos los hechos ocurridos en
        1969, en
        los
        disturbios de Stonewall , New York  lo cual dio inicio a una lucha mancomunada contra la discriminación
        y
        hostigamiento.</p>

      <p class="justify opensans">
        Nuestra institución, busca continuamente el fortalecimiento del acceso a la justicia, como derecho de
        acceso efectivo.
        Parte importante del nuestra institución lo conforma el Centro Social Cultural Agrupación Nefertiti,
        quienes sonparte del Consejo de Sociedad Civil y que han sido parte fundamental de nuestra lucha para el
        acceso
        a la
        justicia.
      </p>

      <p class="text-justify opensans text-bold">
        Nuestra institución, en conjunto con las demás corporaciones del país crearon un Glosario  de
        discapacidad, género y diversidad, en el año 2022, del cual se desprenden algunos de los siguientes
        conceptos:
      </p>

      <ul class="gobcl">
        <li class="text-justify py-2">
          <span class="text-bold">Demisexuales:</span>
          Engloba a las personas que se sienten atraídas sexualmente por otros individuos con quien ha
          establecido
          previamente un vínculo emocional. Sin importar si se identifican como hombres o mujeres.
        </li>

        <li class="text-justify py-2">
          <span class="text-bold">Asexuales:</span>
          Persona que no siente deseo ni atracción sexual, independientemente de que le guste uno u otro
          género.
        </li>

        <li class="text-justify py-2">
          <span class="text-bold">Binarismo de género:</span>
          Es la idea de que solo hay dos géneros posibles, masculino y femenino. Cuando hablamos de “no
          binarismo”
          se trata de salirse de lo típicamente masculino o femenino, esto es, salirse de este binarismo de
          género. De este modo, podremos comprender mejor a aquellas personas que deciden declararse no
          binarias y
          sabremos en que consiste.
        </li>

        <li class="text-justify py-2">
          <span class="text-bold">Bisexuales:</span>
          Hace referencia a las personas de cualquier identidad de género que se sienten atraídas tanto por
          mujeres y
          hombres, una atracción que puede ser emocional o física.
        </li>

        <li class="text-justify py-2">
          <span class="text-bold">Cisgénero:</span>
          Se refiere a las personas cuya identidad y expresión de género coinciden con el sexo biológico que
          se
          les
          asigno con cuando nacieron.
        </li>

        <li class="text-justify py-2">
          <span class="text-bold">Demisexuales:</span>
          Engloba a las personas que se sienten atraídas sexualmente por otros individuos con quien ha
          establecido
          previamente un vínculo emocional. Sin importar si se identifican como hombres o mujeres.
        </li>

        <li class="text-justify py-2">
          <span class="text-bold">Sepiosexual:</span>
          Se siente atraído por la inteligencia o cualidades y valores morales de otras personas.
        </li>

        <li class="text-justify py-2">
          <span class="text-bold">Sexo:</span>
          Características biológicas que definen a una persona como un ser masculino o femenino al momento de
          nacer.
        </li>

        <li class="text-justify py-2">
          <span class="text-bold">Transgénero:</span>
          Distintas maneras en que las identidades de género de las personas pueden diferir del género que se
          les
          asignó al nacer. Existen muchos términos diferentes que las personas transgénero utilizan para
          describirse
          a sí mismas. Por ejemplo, en ocasiones, la palabra “transgénero” se acorta como “trans”, “trans*” o,
          incluso, “mujer trans” u “hombre trans”.
        </li>

        <li class="text-justify py-2">
          <span class="text-bold">Transexualidad:</span>
          La transexualidad es un término que refiere a una persona cuya identidad de género no coincida con
          la
          identidad sexual que se le atribuye a partir de criterios biológicos. Las personas transexuales
          desean
          modificar las características sexuales externas que no se corresponden con el género con el que se
          sienten
          identificadas. Esta inconformidad lleva a estas personas a intentar adecuar su cuerpo con el género
          autopercibido y a vivir y ser aceptadas como personas del género opuesto. La adecuación del cuerpo
          supone pasar por una terapia de reemplazo hormonal e incluso quirúrgico. El objetivo final es
          adecuar su
          forma de vida y relaciones sociales, además de su apariencia física mediante cambios anatómicos, es
          decir,
          adecuar su cuerpo al sexo sentido o identidad de género.
        </li>

        <li class="text-justify py-2">
          <span class="text-bold">Travesti:</span>
          Personas que se visten y comportan como el género opuesto, ya sea por su identidad o para realizar
          espectáculos.
        </li>

        <li class="text-justify py-2">
          <span class="text-bold">Gay:</span>
          Es la orientación sexual en la que los hombres que se identifican como tal, se sienten atraídos a
          otros
          hombres.
        </li>

        <li class="text-justify py-2">
          <span class="text-bold">Género no binario:</span>
          Una persona no binaria es aquella que no se identifica con ninguno de los géneros clásicos que
          tradicionalmente se han asociado a los dos sexos clásicos: masculino para hombres y femenino para
          mujeres; por lo contrario, se identifican con identidades que van más allá de estos conceptos.
        </li>

        <li class="text-justify py-2">
          <span class="text-bold">Misoginia:</span>
          Tendencia ideológica y psicológica de odio hacia la mujer que se manifiesta en actos violentos y
          crueles
          contra ella por su género.
        </li>

        <li class="text-justify py-2">
          <span class="text-bold">Queer:</span>
          Es la elaboración teórica de la disidencia sexual y la de-construcción de las identidades
          estigmatizadas,
          que
          a través de la resignificación del insulto consigue reafirmar que la opción sexual distinta es un
          derecho
          humano. Describe una identidad de género y sexual diferente a la heterosexual y cisgénero. Las
          personas
          lesbianas, gays, bisexuales y transgénero pueden quizás identificarse con la palabra “queer”.
        </li>

        <li class="text-justify py-2">
          <span class="text-bold">Identidad sexual:</span>
          La forma en que uno se ve a sí mismo en términos de atracción hacia personas de su mismo sexo o
          personas de otro sexo y que se basa en experiencias, pensamientos y reacciones propias y no en una
          definición determinada por el género o sexo de la pareja o parejas sexuales.
        </li>

        <li class="text-justify py-2">
          <span class="text-bold">Orientación sexual:</span>
          Se refiere a quien nos gusta, hacia quien se sienten atraídos/as física, emocional y/o sexualmente.
        </li>

        <li class="text-justify py-2">
          <span class="text-bold">Omnisexualidad:</span>
          Quienes sienten atracción emocional, afectiva y/o romántica hacia todos los géneros.
        </li>

        <li class="text-justify py-2">
          <span class="text-bold">Lesbiana:</span>
          Hace referencia a aquellas personas que se identifican como mujeres y se sienten atraídas por otras
          mujeres.
        </li>

        <li class="text-justify py-2">
          <span class="text-bold">LGBTQIA+:</span>
          Esta sigla significa “lesbianas, gays, bisexuales, transexuales e intersexuales” y se usa
          generalmente
          para referirse a personas con diversas identidades de género u orientación sexual, o a grupos que
          abogan
          por sus derechos humanos. Sin embargo, hay una amplia variedad de expresiones que se usan a
          menudo para referirse a estas comunidades, incluidas LGBTQ (personas lesbianas, gays, bisexuales,
          transgénero y “queer” o “questioning”) o LGBT+ (el símbolo de la suma incluye a otros grupos, como
          las
          personas asexuales, intersexuales, “queer”, “questioning”), entre otros.
        </li>
      </ul>

