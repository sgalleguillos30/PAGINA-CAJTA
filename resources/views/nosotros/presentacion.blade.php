                <h3 class="title">Nuestros principios</h3>
                <dl class="opensans justify definicion">
                    <dt>Igualdad e Inclusión</dt>
                    <dd>Toda persona, sin distinción, tiene derecho a disfrutar de todos los derechos humanos, incluidos el derecho a la igualdad de trato ante la ley y el derecho a ser protegido contra la discriminación por diversos motivos.</dd>

                    <dt>Opción por los más vulnerables</dt>
                    <dd>Es deber del Estado, a través de este servicio, establecer condiciones para garantizar el acceso a la justicia de las personas o grupos más vulnerables de la población, focalizando las políticas públicas en éstos, facilitando los mecanismos necesarios para la adecuada defensa de sus derechos y asegurando de esta forma la efectividad de su tutela ante el sistema judicial.</dd>

                    <dt>Integralidad</dt>
                    <dd>El principio de Integralidad del servicio de asistencia jurídica consistirá en proveer todas las acciones y los medios necesarios para que el Usuario reciba un servicio que incorpore contenidos que incluyan todos los aspectos, daños y dificultades que se presentan en una vulneración de derechos.</dd>

                    <dt>Profesionalización / Especialización</dt>
                    <dd>El Sistema de Acceso a la Justicia, proveerá, un servicio profesional de orientación, promoción de derechos, asesoría, reparación y representación, que incorpore altos estándares técnicos en la formulación e implementación de soluciones eficaces, eficientes, oportunas e integrales a los conflictos que afecten a sus usuarios. Además, otorgará un servicio especializado, cuando se requiera.</dd>

                    <dt>Desarrollo de Personas</dt>
                    <dd>Se debe propiciar y mantener espacios, sistemas y ambientes de trabajo sanos y amigables, que garanticen el desarrollo y fortalecimiento de los talentos de sus profesionales, técnicos y administrativos.</dd>
                    <dd>Además, implementar planes que detecten y potencien las habilidades y destrezas de sus funcionarios, generando las condiciones y facilidades necesarias, para que éstas se consoliden, desarrollen y proyecten, tanto en la gestión como en la prestación de los servicios que son concordantes con la misión institucional.</dd>

                    <dt>Desconcentración / Descentralización</dt>
                    <dd>El sistema de asistencia jurídica, debe tener una estructura funcionalmente descentralizada y desconcentrada territorialmente, en términos de gestión y administración (de personal, presupuestaria, etc.), en el marco de las orientaciones e instrucciones que impartan quienes están encargados de la conducción del sistema en general.</dd>
                    <dd>Esto permite la presencia institucional en las distintas zonas del territorio; la toma de decisiones en niveles más cercanos a los usuarios, lo que la hace más rápida; que el sistema y el servicio sean adecuados a los usuarios, al ser posible tener una gestión a nivel local, recogiendo la realidad de la zona y de su gente. Además, hace más efectiva la participación interna de los miembros de la organización, con los beneficios que ello conlleva.</dd>

                    <dt>Accesibilidad</dt>
                    <dd>Toda persona, cualquiera sea su edad, sexo, etnia o condición, debe tener la posibilidad de conocer y ejercer sus derechos, los de su grupo familiar, comunidad y/o grupo de pertenencia y protegerlos en caso de ser amenazados. La asistencia jurídica debe contemplar acciones afirmativas que permitan, efectivamente, que todos tengan acceso.</dd>
                    <dd>El principio de accesibilidad hace alusión además a la accesibilidad profesional o inmediación, que corresponde a la posibilidad de toda persona de tener contacto directo con un profesional competente, eliminando intermediaciones innecesarias.</dd>

                    <dt>Flexibilidad</dt>
                    <dd>El sistema de asistencia jurídica debe tener la capacidad de responder adecuadamente a los requerimientos y desafíos de su entorno, siempre dinámico y complejo, y permanecer en el tiempo.</dd>
                    <dd>Ese requiere contar con mecanismos de adaptación, que permitan adecuar las estrategias de acción a las realidades locales, institucionales y personales. Se trata de responder en forma diferenciada a los desafíos de la realidad, especialmente desde la óptica de satisfacción de las necesidades de la comunidad y sus características particulares.</dd>

                    <dt>Modernización</dt>
                    <dd>El acceso a la justicia debe incluir en todos sus aspectos, la tecnología disponible para tener información, orientación y servicios jurídicos que faciliten y hagan efectiva, para todas las personas, la garantía de la igual protección de la ley.</dd>
                    <dd>Esto incluye, especialmente la tecnología de las comunicaciones, de los equipamientos y de los recintos y medios, por los cuales acceder a una defensa y/u orientación jurídica efectiva, que permita dar a cada persona el acceso igualitario.</dd>
                    <dd>El principio de la Modernización, supone un esfuerzo constante -por eso debe ser un principio- para que la desigualdad en acceso a la protección jurídica no se produzca por la diferencia constante que va originando el desarrollo tecnológico de los medios y de las comunicaciones.</dd>

                    <dt>Eficacia y Eficiencia</dt>
                    <dd>Entendemos el principio de eficacia como la consecución de los objetivos, metas y estándares orientados a la satisfacción de las necesidades y expectativas del usuario.</dd>
                    <dd>El principio de eficiencia es entendido como la optimización de los resultados alcanzados con relación a los recursos disponibles e invertidos en su consecución.</dd>
                    <dd>El Sistema de Asistencia Jurídica debe actuar en función de sus objetivos, para lograr el resultado esperado en forma eficiente y eficaz. Es decir, el mejor resultado con un uso óptimo y racional de recursos.</dd>

                    <dt>Gestión de calidad y satisfacción del usuario</dt>
                    <dd>En la prestación de sus servicios, el sistema de asistencia jurídica debe tender a conocer las necesidades y <span class="bold">expectativas del usuario</span> y ofrecerle, en consecuencia, un servicio accesible, adecuado, ágil, flexible, apreciable, útil, oportuno, seguro y confiable, en cualquier circunstancia, de tal manera que se sienta comprendido y atendido, con un mayor valor al esperado.</dd>
                    <dd>Entendemos la satisfacción del usuario como el conjunto de acciones que permiten cumplir con calidad, equidad y eficiencia los requisitos, necesidades y exigencias de los beneficiarios del Sistema Público de Asistencia jurídica.</dd>
                </dl>


