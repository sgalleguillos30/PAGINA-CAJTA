                <h3 class="title">Lo que nos inspira.</h3>
                <p class="opensans">La Constitución Política de la República, en el numeral 3 de  su artículo 19, consagra como garantía constitucional la asistencia jurídica y entrega su regulación al imperio de la ley. De este modo, la Ley N°17.995 del año 1981 y, posteriormente, la Ley  N°18.632 del año 1987, crean cuatro Corporaciones de derecho público, sin fines de lucro, cuya finalidad es prestar asistencia jurídica y judicial gratuita a personas de escasos recursos y, además, proporcionar los medios para efectuar la práctica necesaria para el ejercicio de la profesión, a los postulantes a obtener el título de Abogado, de conformidad con lo preceptuado en el numeral 5 del artículo 523 del Código Orgánico de Tribunales.</p>
                <p class="opensans">Dentro del ordenamiento jurídico, desde una mirada general, las gestiones judiciales ejecutadas por los funcionarios dependientes de las Corporaciones de Asistencia Judicial están sujetas al Código de Procedimiento Civil, el Código Orgánico de Tribunales, la Ley N° 18.120 que establece las normas de comparecencia en juicio y la Ley N° 20.886 que modifica el Código de Procedimiento Civil, para establecer la tramitación digital de los procedimientos judiciales</p>

