                <h3 class="title">Origen de la Corporación</h3>
                <p class="opensans">La Corporación de Asistencia Judicial de las Regiones de Tarapacá y Antofagasta en adelante CAJTA o la Corporación fue creada en virtud de la Ley 18.632, del 24 de Julio de 1987, siendo continuadora legal de la Corporación de Asistencia Judicial de la Región de Valparaíso en relación a los Consultorios de Asistencia Jurídica que ésta poseía en la Primera y Segunda Regiones, así como también respecto del personal de dichos Consultorios.</p>

                <p class="opensans">La Ley 18.632 que creó la CAJTA facultó al Presidente de la República para que, mediante decreto expedido a través del Ministerio de Justicia, estableciera los estatutos por los cuales se regiría, lo que se cumplió con la dictación del DFL N°1 del Ministerio de Justicia, de fecha 17 de febrero de 1988, que regula su objeto y duración, el Consejo Directivo, el Director General y el Patrimonio.</p>

