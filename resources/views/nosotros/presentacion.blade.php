                <h3 class="title">Lo que nos define e identifica: <br />Nuestros Valores.</h3>
                <p class="mb-0 opensans">Los valores que nos inspiran son los siguientes:</p>
                <dl class="opensans justify definicion">
                    <dt>Probidad</dt>
                    <dd>Consiste en observar, en todos los procesos de gestión y desarrollo de personas al interior de la Corporación, una conducta funcionaria intachable con preeminencia del interés general sobre el particular.</dd>

                    <dt>Respeto</dt>
                    <dd>Conducta de buen trato en las relaciones interpersonales en el espacio laboral, en consideración a la dignidad humana, independiente del cargo o rol dentro de la institución. Se reconoce la diversidad y los distintos puntos de vista, propendiendo al cambio cultural en conciliación con la vida laboral y familiar de las funcionarias y los funcionarios.</dd>

                    <dt>Vocación de servicio</dt>
                    <dd>Fortalecimiento del rol de servidores públicos a través del mejoramiento de la calidad y eficiencia en la prestación de servicios, tanto a los/as usuarios/as internos como externos de la Institución.</dd>

                    <dt>Profesionalismo</dt>
                    <dd>La búsqueda de resultados de excelencia en el trabajo, orientada a la mejora continua en la calidad del servicio que se presta a la comunidad; a través de la especialización y la obtención de nuevos conocimientos y habilidades.</dd>

                    <dt>Compromiso</dt>
                    <dd>El cumplimiento de las tareas a cabalidad, de manera permanente y en forma integral, con respeto a la función pública, en el contexto de un equipo de trabajo que genere climas laborales nutritivos para el desarrollo y logro de los objetivos institucionales.</dd>

                    <dt>Experiencia</dt>
                    <dd>Actuar con competencia, habilidad y conocimiento.</dd>

                    <dt>Colaboración</dt>
                    <dd>Integración, apoyo mutuo y compañerismo en el trabajo, que favorece las prácticas colaborativas, el trabajo en equipo y permite la generación de sinergias para el logro de los resultados de la Corporación.</dd>

                    <dt>Eficiencia</dt>
                    <dd>Actuar para lograr resultados, optimizando los recursos disponibles.</dd>

                    <dt>Imparcialidad</dt>
                    <dd>Actuar sin sesgos, prejuicios o tratos diferenciados de algún tipo hacia ninguna persona.</dd>

                    <dt>Efectividad</dt>
                    <dd>Actuar para lograr los resultados esperados.</dd>
                </dl>
