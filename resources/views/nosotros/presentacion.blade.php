                <h3 class="title">Nuestra razón de ser.</h3>
                <h4 class="bold">Misión</h4>
                <p class="opensans">Somos el servicio público autónomo que garantiza a todas las personas en condiciones de vulnerabilidad la igual protección de la ley en el ejercicio de sus derechos, a través de un equipo humano competente y altamente comprometido.</p>

                <h4 class="bold">Visión</h4>
                <p class="opensans">Ser referentes en el ámbito del acceso a la justicia, generando y liderando políticas públicas relacionadas con prevención, promoción, orientación, protección y defensa de los derechos de las personas, brindando a nuestros usuarios un servicio de calidad, cercano e innovador.</p>

                <h4 class="bold">Nuestros usuarios</h4>
                <p class="opensans">Tendrán derecho a los servicios que brinda el Sistema de Asistencia Jurídica, las personas, agrupaciones, asociaciones u organizaciones que se encuentren en condiciones de vulnerabilidad en el acceso a la Justicia, sin perjuicio de aquellos servicios de carácter universal al que podrán acceder todas las personas que lo requieran.</p>


