                <h3 class="title">Cómo entendemos la Asistencia Jurídica y el Acceso a la Justicia</h3>
                <h4 class="bold">Concepto de Acceso a la Justicia</h4>
                <p class="opensans">El derecho de acceso a justicia constituye uno de los pilares fundamentales de un Estado de Derecho, cuya esencia consiste en poner a disposición de los ciudadanos un sistema capaz de procesar y resolver sus conflictos, a fin de garantizar el ejercicio pleno de sus derechos.</p>
                <p class="opensans">Este es el derecho que tiene toda persona independiente de su condición, de acudir a un sistema que facilite el respecto de sus derechos y posibilite la resolución de conflictos proveyendo  defensa jurídica u otros mecanismos alternativos según corresponda.</p><br />

                <p class="bold">
                    Las Corporaciones de Asistencia Judicial en sus servicios deben contemplar:
                <ul class="opensans">
                    <li>Orientación e Información en Derechos.</li>
                    <li><span class="bold">Solución Colaborativa de Conflictos:</span> prestación de servicios para resolver conflictos en instancias prejudiciales de mediación, negociación, arbitraje y conciliación.</li>
                    <li>Representación y patrocinio judicial.</li>
                    <li>Asistencia Jurídica para el Acceso a la Justicia.</li>
                    <li>Educación, promoción y difusión de derechos, desarrollo de redes sociales locales y de estrategias de trabajo comunitario que faciliten la prevención de conflictos y la intervención intersectorial.</li>
                    <li>Atención integral a víctimas de delitos.</li>
                    <li>En general, todas las acciones tendientes a facilitar y asegurar la prevención o solución de conflictos jurídicos y un expedito, eficiente y oportuno Acceso a la Justicia</li>
                </ul>
                </p>

                <p class="bold dl-horizontal">
                    De esta manera, el acceso a la justicia tiene dos dimensiones:
                <ul class="opensans">
                    <li>
                        <span class="bold">Asesoría Jurídica</span>, cuyo objeto es otorgar a todas las personas, independientemente de su condición económica, información jurídica relevante para abordar un conflicto puntual, o una problemática presente o futura, a través de una prestación profesional de carácter individual o colectivo. Para esto, se cuenta con:
                        <ul>
                            <li style="list-style-type: lower-alpha; margin-left: 15px;">Líneas de Orientación e Información en Derechos.</li>
                            <li style="list-style-type: lower-alpha; margin-left: 15px;">Prevención de Conflictos y Promoción de Derechos.</li>
                        </ul>
                    </li>
                    <li>
                        <span class="bold">Asistencia Judicial</span>, que comprende los servicios de carácter resolutivo, a través de los cuales se intenta reconocer y/o ejercer un derecho, poniendo fin a una situación de abuso a través de una sentencia ejecutoriada y, en algunos casos, obtener la reparación del mal causado producto de esta vulneración. Así, la asistencia judicial comprende:
                        <ul>
                            <li style="list-style-type: lower-alpha; margin-left: 15px;">Patrocinio Judicial.</li>
                            <li style="list-style-type: lower-alpha; margin-left: 15px;">Servicio de Solución Colaborativa de Conflictos (cuya solución generalmente es homologada en sede judicial).</li>
                            <li style="list-style-type: lower-alpha; margin-left: 15px;">Apoyo integral, comprendiendo en este último caso a la atención integral a víctimas de delitos violentos, y la representación jurídica de niños, niñas y adolescentes insertos en situación de cuidado alternativo, incorporada como una nueva línea de atención especializada desde el año 2018.</li>
                        </ul>
                    </li>
                </ul>
                </p>

