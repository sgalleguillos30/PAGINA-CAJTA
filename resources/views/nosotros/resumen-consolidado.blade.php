                <h3 class="title">Nuestra Atención</h3>
                <p>Nuestra labor está orientada a brindar acceso a la justicia por medio de una atención con perspectiva de género, informando a las personas sobre sus derechos, con un énfasis en lo preventivo y centrado en la atención preferente de los grupos más vulnerables de la sociedad centrado en las personas con discapacidad, las personas adultas y adultos mayores, los niños, niñas y adolescentes, como también en las necesidades de migrantes y personas privadas de libertad.</p>
