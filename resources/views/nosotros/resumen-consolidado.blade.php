                <h3 class="title">Principales Materias de Atención</h3>

                <h4 class="bold">Civil o Patrimonial</h4>
                <p>Acciones contenciosas y voluntarios, como son demandas de indemnización de perjuicios, cobro de pesos, reivindicatorias, deslindes y cerramientos, arrendamientos, comodatos, precarios, tercerías, interdictos posesorios, cambio de nombre, interdicciones, gestiones voluntarias en general.</p>

                <h4 class="bold">Familia</h4>
                <p>Pensiones de alimentos, acciones de filiación, régimen comunicacional, divorcios y separaciones, cuidado personal, medidas de protección, defensa en procesos de Violencia Intrafamiliar.</p>

                <h4 class="bold">Penal</h4>
                <p>Víctimas de delitos violentos, tales como homicidios, lesiones graves, robo con intimidación, delitos sexuales, cuasidelitos de lesiones graves, manejos en estado de ebriedad con resultado de muerte y/o lesiones graves gravísimas y trata de personas. Los relativos a la Justicia Militar o Sistema Penal Antiguo en los casos como unificación de penas, entre otras (Consultorios Jurídicos).</p>

                <h4 class="bold">Conflictos del Mundo Laboral</h4>
                <p>Despidos injustificados o improcedentes, desafueros maternales o sindicales, tutela de derechos fundamentales y cobranza de prestaciones adeudadas, entre otras.</p>

                <h4 class="bold">Infancia</h4>
                <p>Restituimos los derechos de todos los Niños, Niñas y adolescentes vulnerados, mediante el asesoramiento, representación y el derecho a defensa jurídica letrada, especializada y autónoma e interdisciplinaria asegurando su acceso a la justicia, mediante el acceso a mecanismos judiciales y/o administrativos en el marco de un debido proceso y desde una perspectiva centrada en los derechos humanos de los niños, niñas y adolescentes.</p>



