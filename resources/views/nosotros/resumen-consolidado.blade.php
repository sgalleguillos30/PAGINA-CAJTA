                <h3 class="title">¿Qué es la Corporación?</h3>
                <p>La Corporación de Asistencia Judicial DE Arica, Tarapacá y Antofagasta es un ser vicio público autónomo relacionado con el Ministerio de Justicia y Derechos Humanos, que garantiza a todas las personas en condiciones de vulnerabilidad la igual protección de la ley en el ejercicio de sus derechos, a través de un equipo humano competente y altamente comprometido, integrado por abogado/as, trabajadores/as sociales, psicólogo/as, técnico/as jurídicos y personal administrativo.</p>

                <div class="texto-separacion"></div>

                <h3 class="title">Líneas de Servicios.</h3>
                <h4 class="bold">Orientación e Información en Derechos</h4>
                <p>Servicio al cual pueden acceder todas las personas. Se brinda orientación jurídica y social respecto de un problema o conflicto, y se entrega información sobre deberes y derechos, y la manera más adecuada de hacerlos efectivos.</p>

                <h4 class="bold">Solución Colaborativa de Conflictos</h4>
                <p>Consiste en la atención de profesionales especializados en conciliación y mediación, cuyo objetivo es ofrecer mediante el diálogo, la oportunidad de dar solución a conflictos familiares, civiles, penales, vecinales y comunitarios, sin que sea necesario llegar a tribunales. Los acuerdos que surjan serán resultado de la voluntad de todos los intervinientes, contribuyendo de este modo a la paz social.</p>

                <h4 class="bold">Prevención de Conflictos y Promoción de Derechos</h4>
                <p>Consiste en la prevención de conflictos, promoción y difusión de los derechos y deberes de las personas, a través de las actividades de carácter comunitario dirigidas a la población general, como talleres, charlas, plazas de justicia, entre otra.</p>

                <h4 class="bold">Representación y Patrocinio Judicial</h4>
                <p>Consiste en la presentación de demandas y otras acciones ante los Tribunales de Justicia, para aquellas personas que presentan conflictos de relevancia jurídica y social, y que no han podido llegar a acuerdo. Este servicio está destinado a quienes carecen de los medios para contratar a un abogado que los represente.</p>

                <h4 class="bold">Representación de Niños, Niñas y Adolescentes</h4>
                <p>La Corporación interviene respecto de dos ámbitos de acción mediante líneas especializadas:
                    <ul class="opensans">
                        <li><span class="text-bold">Programa mi Abogado:</span> Vulneración de derechos (polivictimización) y la extensión y/o magnitud del daño causado(cronificación) de Niños Niñas y adolescentes.</li>
                        <li><span class="text-bold">La Niñez y la Adolescencia se Defienden:</span> Vulneraciones de mediana o leve entidad, o sean parte de causa contenciosa que afecte intereses.</li>
                    </ul>
                </p>


